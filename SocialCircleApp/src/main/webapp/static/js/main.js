var ALERT_TITLE = ' Error!';
var ALERT_BUTTON_TEXT = "Ok";

if (document.getElementById) {
    window.alert = function(txt) {
        createCustomAlert(txt);
    }
}

function createCustomAlert(txt) {
    d = document;

    if (d.getElementById("modalContainer")) return;

    mObj = d.getElementsByTagName("body")[0].appendChild(d.createElement("div"));
    mObj.id = "modalContainer";
    mObj.style.height = d.documentElement.scrollHeight + "px";

    alertObj = mObj.appendChild(d.createElement("div"));
    alertObj.id = "alertBox";
    if (d.all && !window.opera) alertObj.style.top = document.documentElement.scrollTop + "px";
    alertObj.style.left = (d.documentElement.scrollWidth - alertObj.offsetWidth) / 2 + "px";
    alertObj.style.visiblity = "visible";

    var elem = d.createElement("i");
    elem.className = "fa fa-exclamation-circle";

    h1 = alertObj.appendChild(d.createElement("h1"));

    h1.appendChild(elem);
    h1.appendChild(d.createTextNode(ALERT_TITLE));

    msg = alertObj.appendChild(d.createElement("p"));
    //msg.appendChild(d.createTextNode(txt));
    msg.innerHTML = txt;

    btn = alertObj.appendChild(d.createElement("a"));
    btn.id = "closeBtn";
    btn.appendChild(d.createTextNode(ALERT_BUTTON_TEXT));
    btn.href = "#";
    btn.focus();
    btn.onclick = function() {
        removeCustomAlert();
        return false;
    }

    alertObj.style.display = "block";

}

function removeCustomAlert() {
    document.getElementsByTagName("body")[0].removeChild(document.getElementById("modalContainer"));
}

//HELPER METHOD
function toJson($form) {
    var serialized = $form.serializeArray();
    var s = '';
    var data = {};
    for (s in serialized) {
        data[serialized[s]['name']] = serialized[s]['value']
    }
    var json = JSON.stringify(data);
    return json;
}

function handleAjaxError(response) {
    var response = JSON.parse(response.responseText);
    alert(response.message);
}

function handleCustomError(response) {
    alert(response);
}

function getSuccessSnackbar(message) {
    var x = document.getElementById("snackbar");
    x.innerHTML = message;
    x.className = "show";
    setTimeout(function() {
        x.className = x.className.replace("show", "");
    }, 3000);
}

function roundN(value, digits) {
   var tenToN = 10 ** digits;
   return (Math.round(value * tenToN)) / tenToN;
}

function getErrorSnackbar(message) {
    var x = document.getElementById("errorSnackbar");
    x.innerHTML = message;
    x.className = "show";
    setTimeout(function() {
        x.className = x.className.replace("show", "");
    }, 3000);
}

function getAuth(needsAuth) {
    $.ajax({
        url: getBaseUrl() + "/api/user/auth",
        type: "GET",
        success: function(response) {
            populateAuthData(needsAuth, response)
        },
        error: handleAjaxError
    });
}

function populateAuthData(needsAuth, data) {
    if (needsAuth && !data.isAuthenticated) {
        alert("You need to be logged in");
    } else if (data.isAuthenticated) {
        document.getElementById("navbar-main-user-name").innerHTML = data.user.name;
        document.getElementById("navbar-secondary-user-name").innerHTML = data.user.name;
        document.getElementById("navbar-main-user-role").innerHTML = data.user.userRole;
    } else {
        var nav = document.getElementById("logged-in-nav-dropdown");
        nav.style.display = "none";
        document.getElementById("login-link-nav-holder").innerHTML = "<button type=\"button\" onclick=\"location.href='/cocourse/login'\" class=\"btn btn-outline-primary\">Login</button>";
    }
}

function getCheckAuthentication(needsAuth) {
    getAuth(needsAuth);
}

function logout() {
    $.ajax({
        url: getBaseUrl() + "/api/logout",
        type: "POST",
        success: function(response) {
            getSuccessSnackbar("Logged out. Redirecting to home...");
            //await delay(3000);
            window.location.replace(getBaseUrl());
        },
        error: handleAjaxError
    });
}

